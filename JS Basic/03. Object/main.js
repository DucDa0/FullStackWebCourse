// ===Lí thuyết=== //
// 1. Các khái niệm căn bản
// + Object được dùng đẻ mô tả một vật thông qua các thuộc tính(Property) của vật đó
// 2. Syntax
/**
 * var object_name={
 * key: value,
 * key: value,
 *  ....
 * }
 * + Với key là tên thuộc tính
 * + value: giá trị của thuộc tính, có thể là number, string, object, function,...
 */
// 3. Các thao tác trên Object
var myCat={
    name:'mad',
    age: 4
}
// - Truy xuất đến 1 thuộc tính: myCat.name hoặc myCat['name']
// - Xóa 1 thuộc tính: delete myCat.age
// - Thêm 1 thuộc tính: myCat['weight']=1.5;

